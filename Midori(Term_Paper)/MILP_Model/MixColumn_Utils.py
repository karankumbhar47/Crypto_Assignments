def equalArray(output_matrix, expected_matrix):
    if len(output_matrix) != len(expected_matrix):
        return False
    if len(output_matrix[0]) != len(expected_matrix[0]):
        return False
    for i in range(0, len(expected_matrix)):
        for j in range(0, len(expected_matrix)):
            if output_matrix[i][j] != expected_matrix[i][j]:
                return False
    return True


